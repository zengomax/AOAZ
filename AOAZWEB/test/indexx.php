    <h2>Zer da garapen jasangarria?</h2>
<br>
Kontzeptu hori 1987an agertu zen lehenengoz, Brundtland Txostena argitaratu zenean, eta alerta bat sortu zuen garapen ekonomikoaren eta globalizazioaren ondorioz ingurumenean sortzen ari ziren ondorio negatiboei buruz. Garapen jasangarria egungo sorkuntzaren beharrak asetzeko gaitasuna duen garapenari dagokio; horretarako, baliabide naturalak kontsumitu behar dira, etorkizuneko belaunaldietarako baliabide horien eskuragarritasuna arriskuan jarri gabe. 
<br>
<br>Adibidez, zuhaitzak moztea jarduera jasangarritzat jo daiteke, baldin eta birpopulatzea bermatzen bada. Aitzitik, petrolioa kontsumitzea gaur egun ez da garapen jasangarriari lotutako jarduera bat, ezin baitugu hurrengo belaunaldientzat nahiko berehala konpondu, eta hori giza jarduera gehienekin gertatzen da, gaur egun garatzen diren moduan.</div>
