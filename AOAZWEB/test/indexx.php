  <div class="col-sm-7"style="margin-left:20px "><h2>Garapen jasangarria vs. jasangarritasuna</h2>
<br>
Oso antzeko hitzak izan daitezkeen arren, ez dira nahastu behar. Garapen jasangarriak iraunkortasuna edo sostengarritasuna lortu nahi du, eta, beraz, garapen mota horren helburu bat da. Horrela, garapen honek pertsonen ingurunea eta bizi-kalitatea hobetzea lortu nahi du, etorkizuneko belaunaldien eta planetaren beraren biziraupenerako sostengua arriskuan jarri gabe.<br><br>


Ekologiaren iraunkortasuna sistema biologikoetan espezieak bizi diren inguruneko baliabideekin orekan egoteko prozesu gisa ezagutzen da.
